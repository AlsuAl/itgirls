let select=document.getElementById("select-color")
function changebg(){
 if(select.value=='black'){
    document.body.style.backgroundColor ="rgb(105,105,105)";
 } 
 if(select.value=='white'){
    document.body.style.backgroundColor ="rgb(255,255,255)";
 } 
 if(select.value=='blue'){
    document.body.style.backgroundColor ="rgb(65,105,225)";
 } 
}
